def print_banner(project="Project", task_flow="unknown", mode="Deploy"):
    use_color = "--no-color" not in sys.argv

    print()
    for line in LOGO_LINES:
        print(f"  {line}")
    print()
    print()
    print()
    if use_color:
        print(f"    {BOLD}MICROSOFT FABRIC TASK FLOWS{RESET}")
    else:
        print("    MICROSOFT FABRIC TASK FLOWS")
    print("    Deploy Fabric architectures to production")
    print()
    print(f"    Project:    {project}")
    print(f"    Task Flow:  {task_flow}")
    print(f"    Mode:       {mode}")
    print(f"    {'─' * 60}")
    print()


if __name__ == "__main__":
    print_banner(
        project="Energy Field Intelligence",
        task_flow="medallion",
        mode="Deploy to Fabric",
    )
